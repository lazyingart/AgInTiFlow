export function normalizeLanguage(language) {
  const raw = String(language || "").replace("_", "-");
  if (/^ar/i.test(raw)) return "ar";
  if (/^de/i.test(raw)) return "de";
  if (/^es/i.test(raw)) return "es";
  if (/^fr/i.test(raw)) return "fr";
  if (/^ja/i.test(raw)) return "ja";
  if (/^ko/i.test(raw)) return "ko";
  if (/^ru/i.test(raw)) return "ru";
  if (/^vi/i.test(raw)) return "vi";
  if (/^zh-(tw|hk|mo|hant)$/i.test(raw)) return "zh-Hant";
  if (/^zh/i.test(raw)) return "zh-Hans";
  return languages.some((item) => item.code === raw) ? raw : "en";
}

export function languageMeta(language) {
  const code = normalizeLanguage(language);
  return languages.find((item) => item.code === code) || languages[0];
}

export function t(language, key, params = {}) {
  const code = normalizeLanguage(language);
  const dictionary = docsUi[code] || docsUi.en;
  const template = dictionary[key] || docsUi.en[key] || "";
  return Object.entries(params).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, String(value)), template);
}
