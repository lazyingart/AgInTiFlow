function deriveDocs(language) {
  const alias = aliasSource[language];
  if (!alias) return {};
  const source = docs[alias.from] || {};
  return Object.fromEntries(
    Object.entries(source).map(([id, markdown]) => [
      id,
      alias.replacements.reduce((text, [from, to]) => text.replaceAll(from, to), markdown),
    ])
  );
}

